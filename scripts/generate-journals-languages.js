const fs = require('fs');
const path = require('path');

const externalAssetsDir = path.join(__dirname, '../external-assets');
const outputPath = path.join(__dirname, '../src/config/journals-languages-generated.ts');

const DEFAULT_LANGUAGE = 'en';
const DEFAULT_ACCEPTED = ['en', 'fr'];

function parseEnvFile(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8');
  const env = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;
    const key = trimmed.slice(0, eqIdx).trim();
    const raw = trimmed.slice(eqIdx + 1).trim();
    env[key] = raw.replace(/^["']|["']$/g, '');
  }
  return env;
}

try {
  const files = fs.readdirSync(externalAssetsDir).filter(f => f.startsWith('.env.local.'));

  const result = {};

  for (const file of files) {
    const journalCode = file.replace('.env.local.', '');
    const env = parseEnvFile(path.join(externalAssetsDir, file));

    const defaultLang = env['NEXT_PUBLIC_JOURNAL_DEFAULT_LANGUAGE'] || DEFAULT_LANGUAGE;
    const accepted = env['NEXT_PUBLIC_JOURNAL_ACCEPTED_LANGUAGES']
      ? env['NEXT_PUBLIC_JOURNAL_ACCEPTED_LANGUAGES'].split(',').map(l => l.trim()).filter(Boolean)
      : DEFAULT_ACCEPTED;

    result[journalCode] = { default: defaultLang, accepted };
  }

  const entries = Object.entries(result)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([code, cfg]) => {
      const accepted = JSON.stringify(cfg.accepted);
      return `  '${code}': { default: '${cfg.default}', accepted: ${accepted} }`;
    })
    .join(',\n');

  const fileContent = `// Auto-generated file. Do not edit manually.
// Generated by scripts/generate-journals-languages.js

export const journalLanguages: Record<string, { default: string; accepted: string[] }> = {
${entries},
};
`;

  fs.writeFileSync(outputPath, fileContent);
  console.log(`Successfully generated ${outputPath} with ${Object.keys(result).length} journals.`);
} catch (err) {
  console.error('Error generating journal languages:', err);
  process.exit(1);
}
